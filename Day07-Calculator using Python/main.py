# operators
# python has different types of operators for different operation.
# To create a calculator we require Arithmatic operators.

print(5 + 5)  # Addition
print(5 - 5)  # Subtraction
print(5 * 5)  # Multiplicaton
print(5 / 5)  # Division
print(5 % 5)  # Modulus
print(5 // 5) # Floor Division
print(5 ** 5) # Exponential

